    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        HotelEmpleados a = new HotelEmpleados();
        //los datos ingresados se colocan en el objeto e instancia de la clase Empleado
        a.setCodigo(Integer.parseInt(codigo.getText()));
        a.setNombre(nombre.getText());
        a.setHora(Integer.parseInt(horas.getText()));
        a.setRol(rol.getText());
        lista.add(a);
        limpiar();
        llenar();
    }//GEN-LAST:event_jButton1ActionPerformed

